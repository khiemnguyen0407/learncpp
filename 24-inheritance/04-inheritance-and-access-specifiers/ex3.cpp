/* Protected inheritance */

/* 
Protected inheritance is the least common method of inheritance. It is almost never used, except in 
very particular cases. With protected inheritance, the public and protected members become 
protected, and private members stay inaccessible.

Because this form of inheritance is so rare, we'll skip the example and just summarize with a 
table:

Access specifier in base class  |   Access specifier when inherited protectedly
===============================================================================
Public	                        |   Protected
Protected	                    |   Protected
Private	                        |   Inaccessible
*/